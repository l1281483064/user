public class User {
    private Integer id;

    public User(Integer id) {
        this.id = id;
    }

    public void setId(Integer id) {

        this.id = id;
    }

    public Integer getId() {

        return id;
    }
    private String name;

    public void setName(String name) {
        
        this.name = name;
    }

    public String getName() {

        return name;
    }
}
